public abstract class ClassesAbstratas {
  /* Para definir qualquer classe ou método como abstrato, basta usar a palavra abstract
   * 
   * Classes abstratas são classes que não podem ser instanciadas, só podem servir como progenitoras
   * 
   * Métodos abstratos são métodos declarados porém não implementados na progenitora. Só podem estar em interfaces ou classes abstratas
   *
   * Uma classe final é uma classe que não pode ser progenitora, é uma classe infértil
   *
   * Um método final não pode ser sobreescrito e é obrigatoriamente herdado
   *
   * Métodos ou classes finais são declarados com a palavra final:
   */
  
  public final void metodo() {
  
  }
}
